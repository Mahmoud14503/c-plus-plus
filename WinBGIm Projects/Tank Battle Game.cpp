void drawIcons(){
    cleardevice();
    for(int r = 0; r < 30; r++){
        for(int c = 0; c < 30; c++){
            switch(positions[r][c]){
            case 0: break;
            /*
            case 1:
            case 2:
            case 3:
            case 4:
            case 5:
            case 6:
            case 7:
            case 8:
            case 9:
            */
            default:
                readimagefile(icons[positions[r][c]].c_str(), c*16,r*16,c*16+16,r*16+16);
                break;
            }
        }
    }
}

bool checkEscapePressed(){

    if(kbhit()){
        char key = getch();
        if(key == 27)
            return true;
    }

    return false;
}


void moveUp(){
    positions[rowYellowTankPosition][colYellowTankPosition] = 0;
    rowYellowTankPosition--;
    positions[rowYellowTankPosition][colYellowTankPosition] = 6;
    drawIcons();
}
void moveDown(){
}
